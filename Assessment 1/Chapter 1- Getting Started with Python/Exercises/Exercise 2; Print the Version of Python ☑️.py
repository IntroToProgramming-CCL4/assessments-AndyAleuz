# Import the sys module to access system parameters and functions
import sys

# Print a string "Python version"
print("Python version")

# Print the Python version, sys.version gives a string containing the version number of the Python interpreter plus additional information.
print(sys.version)

# Print a string "Version info."
print("Version info.")

# Print detailed Python version info using sys.version_info
print(sys.version_info)
