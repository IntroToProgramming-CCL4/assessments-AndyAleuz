# Define a lambda function called 'favorite_book' that takes one parameter 'title'.
# The lambda function prints a message that includes the provided book title.
favorite_book = lambda title: print(f"One of my favorite books is {title}")

# Call the 'favorite_book' function with the argument "Alice in Wonderland".
# This will print a message with the specified book title.
favorite_book("Alice in Wonderland")
favorite_book("Dark Materials")
favorite_book("La belle Savauge")

